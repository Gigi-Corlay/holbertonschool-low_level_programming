#include "main.h"
#include <stdio.h>

/**
 * reset_to_98 - Updates the value of an integer to 98.
 * @n: A pointer to the integer to be updated.
 *
 * Description: This function takes a pointer to an integer as a parameter,
 *              and updates the value it points to by setting it to 98.
 *              This function does not return a value.
 */
void reset_to_98(int *n)
{
	*n = 98;
}
